export function translate(
  language: AppLanguage,
  key: string,
  params?: Record<string, string | number>
): string {
  const target = translations[language] ?? translations.en;
  const fallback = translations.en;

  const resolve = (obj: { [key: string]: TranslationNode }, path: string): string | undefined => {
    const parts = path.split('.');
    let current: TranslationNode | undefined = obj;

    for (const part of parts) {
      if (!current || typeof current === 'string') {
        return undefined;
      }
      current = current[part];
    }

    return typeof current === 'string' ? current : undefined;
  };

  const template = resolve(target, key) ?? resolve(fallback, key) ?? key;

  if (!params) return template;

  return Object.entries(params).reduce((acc, [paramKey, paramValue]) => {
    return acc.replace(new RegExp(`{{${paramKey}}}`, 'g'), String(paramValue));
  }, template);
}
